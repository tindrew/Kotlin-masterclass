fun main(args: Array<String>) {
    var tvShows = "many"
    println(tvShows)
    tvShows = "too many"
    println(tvShows)

    val color = "blue" //val are immutable
    var color2 = "red" //var are normal variables
    println(color)
    println(color2)

    val catName = "My cat's name is \"Fluffy\""
    println(catName)

    var var1 = "Two types of slashes \\ and /."
    println(var1)

    var url = "https://www.google.com"
    println(url)

    var bunnyEars = ("(\\ (\\" )
    println(bunnyEars)
    var bunnyHead = "(-.-)"
    println(bunnyHead)
    var bunnyBody = "o_(\")"
    println(bunnyBody)
}